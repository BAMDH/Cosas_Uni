    /**
    *Busca el atleta que fue seleccionado en la casilla de texto y lo retorna
    * @param copy Identificador utilizado para encontrar el elemento deseado
    */
    private Atleta buscarAtleta(String copy){
        int contador=0;
        boolean encontrado=false;
        while (contador< competencia.getAtletlas().size()&&!encontrado){
            if (competencia.getAtletlas().get(contador).getNombreCompleto().equals(copy)) {
                contador=contador;
                encontrado=true;
            }
            else{
               contador++;
            }
        }
        return competencia.getAtletlas().get(contador);
    }
    
    private void regiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regiActionPerformed
        boolean realizar= true;
        int posicion=0;
        String copy=eAtleta.getSelectedItem().toString();
        String m=marca.getText();
        String me= disciplina.getMedida();
        Atleta at = buscarAtleta(copy);
        int mar=0;
        try{
            mar=Integer.parseInt(m);
        }
        catch(NumberFormatException e){
            realizar=false;
            JOptionPane.showMessageDialog(this,"La marca debe ser un nÃºmero entero","Error",JOptionPane.ERROR_MESSAGE);
        }
        System.out.println(me.equals("m"));
  
        if(realizar){
            while (posicion< prueba.getMarcas().size()&&!realizar){
                if (me.equals("m")){
                    if(prueba.getMarcas().get(posicion).getMarca()> mar){
                        realizar=true;
                    }
                    else{
                       posicion++;
                    }
                }
                else if (me.equals("s")){
                    if(prueba.getMarcas().get(posicion).getMarca()< mar){
                        realizar=true;
                    }
                    else{
                       posicion++;
                    }
                }
                else{
                    posicion++;
                }
            }
            Marca marcaT= new Marca(at.getNombreCompleto(),mar,at.getPaisOrigen(),posicion+1,competencia.getNombre(),prueba.toString(),Integer.parseInt(at.getIdAtleta()),me);
            prueba.getMarcas().add(posicion,marcaT);
            posicion++;
            for (int i =posicion;i<prueba.getMarcas().size();i++){
                prueba.getMarcas().get(posicion).aumentarPosicion();
            }
            posicion=0;
            while (posicion< at.getMarcas().size()&&!realizar){
                if (me.equals(at.getMarcas().get(posicion).getMedida())&& me.equals("m")){
                    if(at.getMarcas().get(posicion).getMarca()< mar){
                        realizar=true;
                    }
                    else{
                       posicion++;
                    }
                }
                
                else if (me.equals(at.getMarcas().get(posicion).getMedida())&& me.equals("s")){
                    if(at.getMarcas().get(posicion).getMarca()> mar){
                        realizar=true;
                    }
                    else{
                       posicion++;
                    }
                }
                
                else{
                    posicion++;
                }
            }
            at.getMarcas().add(posicion, marcaT);
            JOptionPane.showMessageDialog(this,"Marca registrada");
            dispose();
            MenuPrueba ventana = new  MenuPrueba();
            ventana.setVisible(true);
            ventana.setAtletas(atletas);
            ventana.setCompetencias(competencias);
            ventana.setConfig(config);
            ventana.cargarPanel(competencia, disciplina);
       
        }
    }//GEN-LAST:event_regiActionPerformed

