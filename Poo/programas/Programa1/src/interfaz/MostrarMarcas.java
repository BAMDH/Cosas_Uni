    /**
    *Hereda la clase atleta a la que pertenece esta clase y carga las cajas de seleccion
    *@param pAtleta Atleta
  
    */
    public void cargarPanel(Atleta pAtleta){
        atleta=pAtleta;
        ArrayList<String> es = new ArrayList<String>();
        for(Marca i : atleta.getMarcas()){
            if(!(es.contains(i.getPrueba()))){
                e.addItem(i.getPrueba());
                es.add(i.getPrueba());
            }
        }
    }
    /**
    *Carga el texto que cumple con la condici√≥n dada
    * @param copy Condicion dada
    */
    private void cargarTexto(String copy){
        String mensaje="Atleta: "+atleta.getNombreCompleto()+"\nPrueba: "+copy+"\nCompetencia/Marca/Lugar\n";
        for(Marca i : atleta.getMarcas()){
            if(copy.equals(i.getPrueba())){
                mensaje+=i.porAtleta();
                
            }
        }
        texto.setText(mensaje);
    }
