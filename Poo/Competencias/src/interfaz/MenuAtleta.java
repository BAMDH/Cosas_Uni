    /**
    * Busca un objeto a partir de un identificador, retorna el objeto encontrado perteneciente a la clase especificada
    * @param copy Identificador
    */
    private Atleta buscarAtleta(String copy){
        int contador=0;
        boolean encontrado=false;
        while (contador< atletas.size()&&!encontrado){
            if (atletas.get(contador).getNombreCompleto().equals(copy)) {
                contador=contador;
                encontrado=true;
            }
            else{
               contador++;
            }
        }
        return atletas.get(contador);
    }
    
