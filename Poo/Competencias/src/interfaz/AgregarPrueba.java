    private void OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkActionPerformed
       String nombre=cajaNombre.getText();
       String m=cajaCategoria.getSelectedItem().toString();
       System.out.println(m);
       Prueba at = new Prueba(nombre,m);
       disciplina.getPruebas().add(at);
       dispose();
       MenuPrueba ventana = new  MenuPrueba();
       ventana.setVisible(true);
       ventana.setAtletas(atletas);
       ventana.setCompetencias(competencias);
        ventana.setConfig(config);
        ventana.cargarPanel(competencia, disciplina);
        JOptionPane.showMessageDialog(this,"Elemento agregado\n"+at.toString());
    }//GEN-LAST:event_OkActionPerformed

